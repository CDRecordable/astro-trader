/**
 * Check if a given date falls within a Mercury retrograde period.
 */
export function isRetrograde(date: Date): boolean {
    const ts = date.getTime();
    return MERCURY_RETROGRADES.some(([start, end]) => {
        return ts >= new Date(start).getTime() && ts <= new Date(end).getTime();
    });
}

/**
 * Get the next Mercury retrograde period from a given date.
 */
export function getNextRetrograde(fromDate: Date): { start: Date; end: Date } | null {
    const ts = fromDate.getTime();
    for (const [start, end] of MERCURY_RETROGRADES) {
        const startDate = new Date(start);
        if (startDate.getTime() > ts) {
            return { start: startDate, end: new Date(end) };
        }
    }
    return null;
}

/**
 * Get all retrograde periods that overlap with a date range.
 */
export function getRetrogratesInRange(start: Date, end: Date): { start: Date; end: Date }[] {
    const startTs = start.getTime();
    const endTs = end.getTime();
    return MERCURY_RETROGRADES
        .map(([s, e]) => ({ start: new Date(s), end: new Date(e) }))
        .filter(r => r.end.getTime() >= startTs && r.start.getTime() <= endTs);
}
